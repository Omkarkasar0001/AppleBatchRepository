package while_loop;

public class Q5_A_To_Z_Using_For_loop {
 public static void main(String[]args) {
	char c;                    //Char because ABCD..Z output we need in Char
    for(c='A'; c<='Z'; ++c)    // (Starting Point; End Point ; Increment from Starting)
	System.out.print(c + " ");
  }
}

